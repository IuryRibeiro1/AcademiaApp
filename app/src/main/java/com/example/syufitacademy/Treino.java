package com.example.syufitacademy;

public class Treino {

    private String dias;
    private String treinos;

    public String getDias() {
        return dias;
    }

    public void setDias(String dias) {
        this.dias = dias;
    }

    public String getTreinos() {
        return treinos;
    }

    public void setTreinos(String treinos) {
        this.treinos = treinos;
    }

    public Treino(String dias, String treinos) {
        this.dias = dias;
        this.treinos = treinos;

    }


}

